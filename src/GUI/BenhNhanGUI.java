    private void loadDataToTable() {
        DefaultTableModel model = (DefaultTableModel) tableBenhNhan.getModel();
        model.setRowCount(0); // Clear existing data from the table
        model.setColumnCount(0);
        model.addColumn("Mã bệnh nhân");
        model.addColumn("Tên bệnh nhân");
        model.addColumn("Tuổi");
        model.addColumn("Giới tính");
        model.addColumn("Sức khỏe răng miệng");
        List<BenhNhanDTO> danhSachBenhNhan = BenhNhanBUS.layDanhSachBenhNhan();

        for (BenhNhanDTO benhNhan : danhSachBenhNhan) {
            Object[] rowData = {benhNhan.getMaBenhNhan(), benhNhan.getTenBenhNhan(), benhNhan.getTuoi(), benhNhan.getGioiTinh(), benhNhan.getSucKhoeRangMieng()};
            model.addRow(rowData);
        }
    }

