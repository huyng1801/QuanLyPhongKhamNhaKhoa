    private void tableBenhNhanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableBenhNhanMouseClicked
        // TODO add your handling code here:
        // Lấy chỉ số hàng được chọn
        int selectedRow = tableBenhNhan.getSelectedRow();

        // Kiểm tra nếu có hàng được chọn
        if (selectedRow >= 0) {
            // Lấy giá trị từ các cột của hàng được chọn
            String maNhaSi = tableBenhNhan.getValueAt(selectedRow, 0).toString();
            String tenNhaSi = tableBenhNhan.getValueAt(selectedRow, 1).toString();
            int tuoi = Integer.parseInt(tableBenhNhan.getValueAt(selectedRow, 2).toString());
            String gioiTinh = tableBenhNhan.getValueAt(selectedRow, 3).toString();
            String sucKhoeRangMieng = tableBenhNhan.getValueAt(selectedRow, 4).toString();

            // Điền dữ liệu vào các trường tương ứng
            txtMaBenhNhan.setText(maNhaSi);
            txtTenBenhNhan.setText(tenNhaSi);
            txtTuoi.setText(String.valueOf(tuoi));
            cboGioiTinh.setSelectedItem(gioiTinh);
            txtSucKhoeRangMieng.setText(sucKhoeRangMieng);
        }
    }//GEN-LAST:event_tableBenhNhanMouseClicked

