    private void btnThemCCDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemCCDActionPerformed
        // TODO add your handling code here:
     String maBenhNhan = BenhNhanUtils.chiTietBenhNhanDTO.getMaBenhNhan();
     String maThuoc = cboThuoc.getSelectedItem().toString().split("-")[0];
     String ghiChu = txtGhiChu.getText();
     ChongChiDinhDTO chongChiDinhDTO = new ChongChiDinhDTO(maBenhNhan,maThuoc, ghiChu);
        
        // Gọi phương thức NhaSiBUS.themNhaSi(nhaSiDTO) để thêm nhà sĩ vào cơ sở dữ liệu
        if (ChongChiDinhBUS.themChongChiDinh(chongChiDinhDTO)) {
            JOptionPane.showMessageDialog(this, "Thêm chống chỉ định thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            // Thêm thành công, cập nhật lại dữ liệu trong JTable hoặc làm gì đó khác (nếu cần)
            loadDataToTable();
                loadChongChiDinh();
        } else {
            JOptionPane.showMessageDialog(this, "Thêm chống chỉ định thất bại!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            // Thêm thất bại, hiển thị thông báo lỗi hoặc xử lý lỗi (nếu cần)
        }
    }//GEN-LAST:event_btnThemCCDActionPerformed

