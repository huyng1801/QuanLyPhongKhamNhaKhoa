        // Lấy dữ liệu từ hàng nhà sĩ được chọn
        DefaultTableModel model = (DefaultTableModel) tableChongChiDinh.getModel();
        String maChongChiDinh = model.getValueAt(selectedRow, 0).toString(); // Giả sử cột MaNhaSi ở cột đầu tiên

        // Hiển thị hộp thoại xác nhận xóa nhà sĩ
        int option = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xóa chống chỉ định này?", "Xác nhận", JOptionPane.YES_NO_OPTION);
        if (option == JOptionPane.YES_OPTION) {
            // Nếu người dùng chọn Yes, thực hiện xóa nhà sĩ từ CSDL
            boolean isXoaNhaSiSuccess = ChongChiDinhBUS.xoaChongChiDinh(maChongChiDinh);

            if (isXoaNhaSiSuccess) {
                // Nếu xóa thành công, cập nhật lại dữ liệu trong JTable
                model.removeRow(selectedRow);
                JOptionPane.showMessageDialog(this, "Xóa chống chỉ định thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                loadDataToTable();
                loadChongChiDinh();
            } else {
                // Nếu xóa thất bại, hiển thị thông báo lỗi
                JOptionPane.showMessageDialog(this, "Xóa chống chỉ định thất bại!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            }
    }           
