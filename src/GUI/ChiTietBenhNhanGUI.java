    private void loadChongChiDinh(){
            DefaultTableModel model = (DefaultTableModel) tableChongChiDinh.getModel();
        model.setRowCount(0); // Clear existing data from the table
        model.setColumnCount(0);
        model.addColumn("Mã chống chỉ định");
        model.addColumn("Mã thuốc");
        model.addColumn("Tên thuốc");
        model.addColumn("Ghi chú");
        BenhNhanUtils.chongChiDinhDTO = BenhNhanBUS.layChongChiDinhTheoMaBenhNhan(BenhNhanUtils.chiTietBenhNhanDTO.getMaBenhNhan());
        List<ChongChiDinhDTO> danhSachChongChiDinh = BenhNhanUtils.chongChiDinhDTO;

        for (ChongChiDinhDTO chongChiDinh : danhSachChongChiDinh) {
            Object[] rowData = {chongChiDinh.getMaChongChiDinh(), chongChiDinh.getMaThuoc(),chongChiDinh.getTenThuoc(), chongChiDinh.getGhiChu()};
            model.addRow(rowData);
        }
    }
